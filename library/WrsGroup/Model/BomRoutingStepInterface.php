<?php
/**
 * Interface for all objects representing a BOM routing step.
 *
 * Specifically, this is for items representing the BOM routing steps
 * as found in the BOMRT or similar tables of the A+ ERP software 
 * or related applications.
 * 
 * @category WrsGroup
 * @package Model
 * @author Eugene Morgan <eugenemorgan@wrsgroup.com>
 */
interface WrsGroup_Model_BomRoutingStepInterface
{
    /**
     * Gets the parent item number.
     * 
     * @return string
     */
    public function getParentItemNumber();

    /**
     * Gets the labor hours.
     * 
     * @return float 
     */
    public function getLaborHours();

    /**
     * Gets the operation description.
     * 
     * @return string 
     */
    public function getOperationDescription();
}
